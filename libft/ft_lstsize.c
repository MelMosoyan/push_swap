#include "../push_swap.h"

int	ft_lstsize(t_list *lst)
{
	int		size;
	t_list	*temp;

	size = 0;
	temp = lst;
	while (temp != NULL)
	{
		if (temp->next == NULL)
			break ;
		temp = temp -> next;
		size++;
	}
	size++;
	return (size);
}
